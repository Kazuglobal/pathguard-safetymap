// 記事をスラッグで取得
export function getArticleBySlug(slug: string): SafeMagazineArticle | undefined {
  return ARTICLES.find(article => article.slug === slug)
}

// すべての記事を取得
export function getAllArticles(): SafeMagazineArticle[] {
  return ARTICLES.sort((a, b) =>
    new Date(b.publishedDate).getTime() - new Date(a.publishedDate).getTime()
  )
}

// カテゴリで記事をフィルタ
export function getArticlesByCategory(category: CategoryKey): SafeMagazineArticle[] {
  return ARTICLES.filter(article => article.category === category)
}

// 日付をフォーマット
export function formatDate(dateString: string): string {
  const date = new Date(dateString)
  return `${date.getFullYear()}.${String(date.getMonth() + 1).padStart(2, '0')}.${String(date.getDate()).padStart(2, '0')}`
}
