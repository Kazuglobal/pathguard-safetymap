export function getPromptById(id: string): DisasterPrompt | undefined {
  return allPrompts.find((p) => p.id === id)
}

// カテゴリでプロンプトを取得
export function getPromptsByCategory(category: TargetAudience): DisasterPrompt[] {
  return allPrompts.filter((p) => p.targetAudience === category)
}

