const SORTED_KEYS: readonly string[] = Object.keys(FURIGANA_DICT).sort(
  (a, b) => b.length - a.length,
)

export interface FuriToken {
  /** 表示テキスト（漢字 or そのままの文字列） */
  readonly t: string
  /** ルビ（漢字のときだけ。無ければルビ無し） */
  readonly r?: string
}

/**
 * 文字列を「ルビ付き語」と「素通しテキスト」のトークン列に分割する。
 * 各位置で辞書の最長一致を優先。マッチしない文字は連結して1トークンに。
 */
export function splitFurigana(text: string): FuriToken[] {
  const tokens: FuriToken[] = []
  let buffer = ""
  let i = 0

  while (i < text.length) {
    const key = SORTED_KEYS.find((k) => text.startsWith(k, i))
    if (key) {
      if (buffer) {
        tokens.push({ t: buffer })
        buffer = ""
      }
      tokens.push({ t: key, r: FURIGANA_DICT[key] })
      i += key.length
    } else {
      buffer += text[i]
      i += 1
    }
  }

  if (buffer) tokens.push({ t: buffer })
  return tokens
}
